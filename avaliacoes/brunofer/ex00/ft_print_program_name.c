#include <unistd.h>

void	print_str(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
		write(1, &str[i], 1);
	write(1, "\n", 1);
}

int	main(int argc, char **argv)
{
	(void)argc;
	print_str(argv[0]);
	return (0);
}

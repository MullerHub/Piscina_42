#include<unistd.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int main() 
{
	char src[] = "running";
	int n = 19;
	char dest[n];

	char *teste = ft_strncpy(dest, src, n);

	write(1, teste, n);

	return (0);
}